    public void CargarModeloTabla(){
        
        
       ArrayList<CIngreso>ListarIngresos = ing.ObtenerIngresos();
      int numeroIngresos = ListarIngresos.size();
      modeloTablaIngresos.setNumRows(numeroIngresos);
        for (int i = 0; i < numeroIngresos; i++) {
            CIngreso ingresos = ListarIngresos.get(i);
            int id = ingresos.getIdingreso();
            Double total = ingresos.getTotal_compra();
            String fecha = String.valueOf(ingresos.getFecha_hora());
            String estatus = ingresos.getEstado();
            
           
            modeloTablaIngresos.setValueAt(id, i, 0);
            modeloTablaIngresos.setValueAt(total, i, 1);
            modeloTablaIngresos.setValueAt(fecha, i, 2);
            modeloTablaIngresos.setValueAt(estatus, i, 3);
        }
         
    }
