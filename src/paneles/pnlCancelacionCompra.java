    private void tblIngresosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblIngresosKeyReleased
         if(evt.getKeyCode()==KeyEvent.VK_ESCAPE){
             int seleccionaopcion = tblIngresos.getSelectedRow();
             if(seleccionaopcion < 0){
                 System.out.println("no hay");
             }else{
                 
                  String estatus=String.valueOf(modeloTablaIngresos.getValueAt(seleccionaopcion,3));

                      if(estatus.equals("Cancelada")){
                       ErrorAlert e= new ErrorAlert(new JFrame(), true);
                        e.msj1.setText("ESTA VENTA YA ESTA CANCELADA");
                        e.msj2.setText("");
                        e.msj3.setText("");
                        e.setVisible(true);
                          
                      }else{
                 try {
                    
                       WarningAlert w = new WarningAlert(new JFrame(), true);
                        w.msj1.setText("ESTAS SEGURO°°°");
                        w.msj2.setText("¡DESEAS CANCELAR EL INGRESO?");
                        w.msj3.setText("todos los productos se descontaran");
                        w.setVisible(true);

                        if (w.hecho) {
                     String id=String.valueOf(modeloTablaIngresos.getValueAt(seleccionaopcion,0));
                     obtenerDetalles(id);
//                     ing.eliminarDetalleIngresos(id);
                     borrar(id);
                         SuccessAlert succ  = new SuccessAlert(new Frame(), true);
                succ.msj1.setText("CORRECTO !!!");
                succ.msj2.setText("SE HA CANCELADO");
                succ.msj3.setText("");
                succ.setVisible(true);
                CargarModeloTabla();
                        }
            
                 } catch (SQLException ex) {
                     Logger.getLogger(pnlCancelacionCompra.class.getName()).log(Level.SEVERE, null, ex);
                 }
               }
             
             }

         }
    }//GEN-LAST:event_tblIngresosKeyReleased
 public void filtroBusqueda(JTextField txt) {
        trsFiltro.setRowFilter(RowFilter.regexFilter(txt.getText()));
    }
 
 
 public void borrar(String id){
        try {
            
            ing.eliminarIngresos(id);
        } catch (SQLException ex) {
            Logger.getLogger(pnlCancelacionCompra.class.getName()).log(Level.SEVERE, null, ex);
        }
 }
 
 public void  obtenerDetalles(String id) throws SQLException{
     
     
      ArrayList<CDetalle_ingresos>ListarUsuarios = ing.obtenerDetalles(id);
      int numerosUsusarios = ListarUsuarios.size();
     
        for (int i = 0; i < numerosUsusarios; i++) {
                
           CDetalle_ingresos ingresos = ListarUsuarios.get(i);
           String articulo = String.valueOf(ingresos.getIdarticulo());
           ArrayList<Double> Stocks =  prod.Obtener_stock(articulo);
                for (int j = 0; j < Stocks.size(); j++) {
                    String cadena = Stocks.get(j).toString();
                    double stockingreso = ingresos.getCantidad();
                    double stockreal =Double.parseDouble(Stocks.get(j).toString());
                    double resultado = stockreal - stockingreso;
                    prod.Rest_Stock(articulo, resultado);
                }
            
        }
 
 }

 
 
 
