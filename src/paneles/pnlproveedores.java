        public  void limpiarTabla(){
        int numFilas = modelotablaProveedores.getRowCount();
        if(numFilas > 0){
            for(int i = numFilas - 1; i >= 0; i--){
                modelotablaProveedores.removeRow(i);
            }
        }
    }
    
